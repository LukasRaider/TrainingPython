zaci = ['Ivan' , 'Karel' , 'Radek']
zaci += ['Michal']
print(zaci)


zaci = ['Ivan' , 'Karel' , 'Radek']
zaci.append['Michal']
print(zaci)


zaci = ['Ivan' , 'Karel' , 'Radek']
zaci.extend["Michal" , "Jaroslav", "Hynek"]
print(zaci)


zaci = ['Ivan' , 'Karel' , 'Radek']
print(zaci[-2])

zaci = ['Ivan' , 'Karel' , 'Radek']
zaci.pop(1)
print(zaci)

zaci = ['Ivan', 'Karel', 'Radek' , 'Honza']
zaci.sort()
print(zaci)


zaci = ['Ivan', 'Karel', 'Radek' , 'Honza']
zaci.reverse()
print(zaci)

